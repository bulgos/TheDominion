%% points

P = [0; -4]
Q = [1; -6] 
R = [3; 2]
S = [4; 1] 
T = [2; -1]

mP = [P(1,1)^2, P(1,1)*P(2,1), P(2,1)^2, P(1,1), P(2,1)]'
mQ = [Q(1,1)^2, Q(1,1)*Q(2,1), Q(2,1)^2, Q(1,1), Q(2,1)]'
mR = [R(1,1)^2, R(1,1)*R(2,1), R(2,1)^2, R(1,1), R(2,1)]'
mS = [S(1,1)^2, S(1,1)*S(2,1), S(2,1)^2, S(1,1), S(2,1)]'
mT = [T(1,1)^2, T(1,1)*T(2,1), T(2,1)^2, T(1,1), T(2,1)]'

mX = [0 0 0 0 0]'
mXX = [mX'; mX'; mX'; mX'; mX']

mX = mX + mP + mQ + mR + mS + mT

mXX = mXX + (mP * mP')
mXX = mXX + (mQ * mQ')
mXX = mXX + (mR * mR')
mXX = mXX + (mS * mS')
mXX = mXX + (mT * mT')

A = chol(mXX)

B = A\mX

C = [B(1,1); B(2,1)/2; B(3,1); B(4,1)/2; B(5,1)/2; -1]

